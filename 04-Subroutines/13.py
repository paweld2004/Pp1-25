import math

natural_logarithm = math.log(5)
e_to_the_power_of_3 = math.exp(3)
square_root_7 = math.sqrt(7)
sine_90_degrees = math.sin(math.radians(90))

print('Natural logarithm of 5:', natural_logarithm)
print('e^3:', e_to_the_power_of_3)
print('Square root of 7:', square_root_7)
print('Sine of 90 degrees:', sine_90_degrees)
